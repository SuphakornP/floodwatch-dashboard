export const warningAlerts = sqliteTable("warning_alerts", {
  id: text("id").primaryKey(),
  status: text("status").notNull().default("draft"),
  severity: text("severity").notNull(),
  district: text("district").notNull(),
  titleEn: text("title_en").notNull(),
  titleMy: text("title_my").notNull(),
  titleTh: text("title_th").notNull(),
  bodyEn: text("body_en").notNull(),
  bodyMy: text("body_my").notNull(),
  bodyTh: text("body_th").notNull(),
  sourceName: text("source_name").notNull(),
  sourceUrl: text("source_url"),
  observedAt: text("observed_at"),
  createdAt: text("created_at").notNull(),
  publishedAt: text("published_at"),
  expiresAt: text("expires_at").notNull(),
  createdBy: text("created_by").notNull(),
  triggerKey: text("trigger_key").unique(),
  autoGenerated: integer("auto_generated", { mode: "boolean" }).notNull().default(false),
}, (table) => [
  index("warning_alerts_status_published_idx").on(table.status, table.publishedAt),
  index("warning_alerts_district_expires_idx").on(table.district, table.expiresAt),
]);

export const alertSubscriptions = sqliteTable("alert_subscriptions", {
  id: text("id").primaryKey(),
  endpoint: text("endpoint").notNull().unique(),
  p256dh: text("p256dh").notNull(),
  auth: text("auth").notNull(),
  district: text("district").notNull(),
  language: text("language").notNull(),
  active: integer("active", { mode: "boolean" }).notNull().default(true),
  consentedAt: text("consented_at").notNull(),
  updatedAt: text("updated_at").notNull(),
  lastError: text("last_error"),
}, (table) => [
  index("alert_subscriptions_active_district_idx").on(table.active, table.district),
]);

export const alertDeliveries = sqliteTable("alert_deliveries", {
  id: text("id").primaryKey(),
  alertId: text("alert_id").notNull(),
  subscriptionId: text("subscription_id").notNull(),
  status: text("status").notNull(),
  attemptedAt: text("attempted_at").notNull(),
  responseCode: integer("response_code"),
  error: text("error"),
}, (table) => [
  index("alert_deliveries_alert_idx").on(table.alertId, table.attemptedAt),
]);
